# Adds a tab with \t
tabby_cat = "\tI'm tabbed in."

# Add a new line
persian_cat = "I'm split\non a line."

# Escape a \ by doubling up e.g. \\
backslash_cat = "I'm \\ a \\ cat"

# \t adds a tab before the string with it
# \n\t adds new line plus tab for the next string
# You can use triple """ or triple ''' depending on style
fat_cat = '''
I'll do a list:
\t* Cat food
\t* Fishes
\t* Fishies
\t* Catnip\n\t* Grass
'''

print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)

